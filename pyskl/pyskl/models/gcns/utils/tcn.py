class unitmlp(nn.Module):

    def __init__(self, in_channels, out_channels, kernel_size=5, stride=1, dilation=1, norm='BN', dropout=0, adaptive=True, channel_annention=False, reduce=4, add_tcn=False, merge_after=False):

        super().__init__()

        self.in_channels = in_channels
        self.out_channels = out_channels
        self.norm_cfg = norm if isinstance(norm, dict) else dict(type=norm)
        self.kernel_size=kernel_size
        self.mlp_size = int((kernel_size+1)/2)
        self.dilation=dilation
        self.channel_annention=channel_annention
        self.add_tcn = add_tcn
        self.merge_after = merge_after
        
        pad = (kernel_size + (kernel_size - 1) * (dilation - 1) - 1) // 2
        if in_channels <= 16:
            self.inter_channels = 8
        else:
            self.inter_channels = in_channels // reduce
        if channel_annention==True:
            self.group=self.inter_channels
        else:
            self.group = 1
        # self.con_pre = nn.Conv2d(in_channels,self.inter_channels,1)
        self.conv = nn.Conv1d(
            self.in_channels, 
            self.out_channels,
            kernel_size=self.mlp_size,
            stride=stride,
            dilation=dilation,
            groups = self.out_channels)
        
        if merge_after:
            self.conv1 = nn.Conv2d(out_channels,out_channels,1)
        else:
            self.conv1 = nn.Conv2d(self.out_channels,out_channels,1)
            #
        if self.add_tcn:
            self.conv2 = nn.Conv2d(
                in_channels,
                out_channels,
                kernel_size=(kernel_size, 1),
                padding=(pad, 0),
                stride=(stride, 1),
                dilation=(dilation, 1))
            if adaptive:
                self.alpha = nn.Parameter(torch.zeros(1))
            else:
                self.register_buffer('alpha', torch.ones(1))
                # self.alpha = torch.ones(1)
        self.bn = build_norm_layer(self.norm_cfg, out_channels)[1] if norm is not None else nn.Identity()
        self.drop = nn.Dropout(dropout, inplace=True)
        self.stride = stride
        # self.init_weights()

    def forward(self, x):
        
        x_original = x
        # x = self.con_pre(x)
        B, C, T, V = x.shape
        x = x.permute(0,3,1,2).reshape(B*V,C,T)
        pad = (self.mlp_size + (self.mlp_size - 1) * (self.dilation - 1) - 1) 
        x = F.pad(x,(pad,0))
        # x = x.permute(0,2,1)
        x = self.conv(x)
        x = x.reshape(B,V,self.out_channels,self.group,-1).mean(-2).permute(0,2,3,1)  
        
        if self.add_tcn:
            x_tcn = self.conv2(x_original)
            if self.merge_after:
                x = self.conv1(x)
                x = x + self.alpha * x_tcn
                # x = torch.cat((x_tcn,x),1)  
            else:        
                x = x + self.alpha * x_tcn
                x = self.conv1(x)
        else:
            x = self.conv1(x)

        # x = self.conv1(x)
        x = self.bn(x)
        x = self.drop(x)
        return x

    def init_weights(self):
        conv_init(self.conv)
        conv_init(self.conv1)
        bn_init(self.bn, 1)
        if self.add_tcn:
            conv_init(self.conv2)
